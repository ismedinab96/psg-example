#Ejercicio 2
#    Escribe un programa en Python que convierta las siguientes temperaturas de grados Fahrenheit a grados Celsius:
#    25 ºF
#   300 ºF
#   76 ºF
# Solucion: 
# La formula para relacionar grados centigrados con grados Fahrenheit es:
#  f-32 / 9 = c / 5
# Despejamos C = 5/9*(f-32)

f_25 = 5/9*(25-32)
f_300 = 5/9*(300-32)
f_76= 5/9*(76-32)
print("25°F en °C es: ",f_25)
print("300°F en °C es: ",f_300)
print("76°F en °C es: ",f_76)

